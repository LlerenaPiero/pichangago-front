        CONSTRAINT [FK_OFERTAS_SLOTS] FOREIGN KEY ([ID_SLOT]) REFERENCES [dbo].[SLOTS]([ID_SLOT]),
        CONSTRAINT [CK_OFERTAS_DESCUENTO] CHECK ([PORCENTAJE_DESC] BETWEEN 1 AND 90),
        CONSTRAINT [CK_OFERTAS_PRECIO] CHECK ([PRECIO_OFERTA] >= 0),
        CONSTRAINT [CK_OFERTAS_FECHAS] CHECK ([FECHA_EXPIRA] > [FECHA_INICIO]),
        CONSTRAINT [CK_OFERTAS_ESTADO] CHECK ([ESTADO] IN ('ACTIVO','INACTIVO','EXPIRADO'))
