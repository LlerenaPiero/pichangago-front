        CONSTRAINT [CK_CANCHAS_ESTADO] CHECK ([ESTADO] IN ('DISPONIBLE','MANTENIMIENTO','INACTIVA')),
        CONSTRAINT [CK_CANCHAS_PRECIOS] CHECK ([PRECIO_BASE] >= 0 AND [PRECIO_HORA_PUNTA] >= 0 AND [PRECIO_HORA_VALLE] >= 0)
    );

    CREATE TABLE [dbo].[FOTOS_CANCHA](
        [ID_FOTO]       CHAR(10)       NOT NULL,
        [ID_CANCHA]     CHAR(10)       NOT NULL,
        [URL_FOTO]      VARCHAR(500)   NOT NULL,
        [ES_PRINCIPAL]  BIT            NOT NULL CONSTRAINT [DF_FOTOS_CANCHA_ES_PRINCIPAL] DEFAULT (0),
        [ORDEN]         INT            NOT NULL CONSTRAINT [DF_FOTOS_CANCHA_ORDEN] DEFAULT (1),
        [FECHA_SUBIDA]  DATETIME2(0)   NOT NULL CONSTRAINT [DF_FOTOS_CANCHA_FECHA_SUBIDA] DEFAULT (SYSDATETIME()),
        CONSTRAINT [PK_FOTOS_CANCHA] PRIMARY KEY CLUSTERED ([ID_FOTO]),
        CONSTRAINT [FK_FOTOS_CANCHA_CANCHAS] FOREIGN KEY ([ID_CANCHA]) REFERENCES [dbo].[CANCHAS]([ID_CANCHA]),
        CONSTRAINT [CK_FOTOS_CANCHA_ORDEN] CHECK ([ORDEN] > 0)
    );

    CREATE UNIQUE NONCLUSTERED INDEX [UX_FOTOS_CANCHA_PRINCIPAL]
    ON [dbo].[FOTOS_CANCHA]([ID_CANCHA])
    WHERE [ES_PRINCIPAL] = 1;

    CREATE TABLE [dbo].[HORARIOS](
