        [HORA_INICIO]   TIME(0)        NOT NULL,
        [HORA_FIN]      TIME(0)        NOT NULL,
        [TIPO_PRECIO]   VARCHAR(20)    NOT NULL,
        [ESTADO]        VARCHAR(20)    NOT NULL CONSTRAINT [DF_HORARIOS_ESTADO] DEFAULT ('ACTIVO'),
        [FECHA_CREA]    DATETIME2(0)   NOT NULL CONSTRAINT [DF_HORARIOS_FECHA_CREA] DEFAULT (SYSDATETIME()),
        CONSTRAINT [PK_HORARIOS] PRIMARY KEY CLUSTERED ([ID_HORARIO]),
        CONSTRAINT [FK_HORARIOS_CANCHAS] FOREIGN KEY ([ID_CANCHA]) REFERENCES [dbo].[CANCHAS]([ID_CANCHA]),
        CONSTRAINT [FK_HORARIOS_DUENOS] FOREIGN KEY ([ID_DUENO]) REFERENCES [dbo].[DUENOS]([ID_DUENO]),
        CONSTRAINT [CK_HORARIOS_DIA] CHECK ([DIA_SEMANA] BETWEEN 1 AND 7),
        CONSTRAINT [CK_HORARIOS_HORA] CHECK ([HORA_FIN] > [HORA_INICIO]),
        CONSTRAINT [CK_HORARIOS_TIPO_PRECIO] CHECK ([TIPO_PRECIO] IN ('BASE','PUNTA','VALLE')),
        CONSTRAINT [CK_HORARIOS_ESTADO] CHECK ([ESTADO] IN ('ACTIVO','INACTIVO'))
    );

    CREATE TABLE [dbo].[SLOTS](
        [ID_SLOT]        CHAR(10)       NOT NULL,
        [ID_HORARIO]     CHAR(10)       NOT NULL,
        [ID_CANCHA]      CHAR(10)       NOT NULL,
        [ID_DUENO]       CHAR(10)       NOT NULL,
        [FECHA]          DATE           NOT NULL,
        [HORA_INICIO]    TIME(0)        NOT NULL,
        [HORA_FIN]       TIME(0)        NOT NULL,
        [PRECIO_FINAL]   DECIMAL(8,2)   NOT NULL,
        [ESTADO]         VARCHAR(20)    NOT NULL CONSTRAINT [DF_SLOTS_ESTADO] DEFAULT ('DISPONIBLE'),
        [FECHA_BLOQUEO]  DATETIME2(0)   NULL,
        [FECHA_EXPIRA]   DATETIME2(0)   NULL,
        [FECHA_CREA]     DATETIME2(0)   NOT NULL CONSTRAINT [DF_SLOTS_FECHA_CREA] DEFAULT (SYSDATETIME()),
        CONSTRAINT [PK_SLOTS] PRIMARY KEY CLUSTERED ([ID_SLOT]),
        CONSTRAINT [FK_SLOTS_HORARIOS] FOREIGN KEY ([ID_HORARIO]) REFERENCES [dbo].[HORARIOS]([ID_HORARIO]),
        CONSTRAINT [FK_SLOTS_CANCHAS] FOREIGN KEY ([ID_CANCHA]) REFERENCES [dbo].[CANCHAS]([ID_CANCHA]),
        CONSTRAINT [FK_SLOTS_DUENOS] FOREIGN KEY ([ID_DUENO]) REFERENCES [dbo].[DUENOS]([ID_DUENO]),
        CONSTRAINT [CK_SLOTS_HORA] CHECK ([HORA_FIN] > [HORA_INICIO]),
        CONSTRAINT [CK_SLOTS_PRECIO] CHECK ([PRECIO_FINAL] >= 0),
        CONSTRAINT [CK_SLOTS_ESTADO] CHECK ([ESTADO] IN ('DISPONIBLE','BLOQUEADO','RESERVADO','NO_DISPONIBLE','EXPIRADO'))
    );

    CREATE TABLE [dbo].[RESERVAS](
        [ID_RESERVA]         CHAR(10)       NOT NULL,
        [ID_USER]            CHAR(10)       NOT NULL,
        [ID_SLOT]            CHAR(10)       NOT NULL,
        [ID_HORARIO]         CHAR(10)       NOT NULL,
        [ID_CANCHA]          CHAR(10)       NOT NULL,
        [ID_DUENO]           CHAR(10)       NOT NULL,
        [PRECIO_BASE]        DECIMAL(8,2)   NOT NULL,
        [COMISION_QR]        DECIMAL(8,2)   NOT NULL CONSTRAINT [DF_RESERVAS_COMISION_QR] DEFAULT (0),
        [DESCUENTO]          DECIMAL(8,2)   NOT NULL CONSTRAINT [DF_RESERVAS_DESCUENTO] DEFAULT (0),
        [MONTO_TOTAL]        DECIMAL(8,2)   NOT NULL,
        [ESTADO]             VARCHAR(20)    NOT NULL CONSTRAINT [DF_RESERVAS_ESTADO] DEFAULT ('PENDIENTE'),
        [FECHA_CREA]         DATETIME2(0)   NOT NULL CONSTRAINT [DF_RESERVAS_FECHA_CREA] DEFAULT (SYSDATETIME()),
        [FECHA_CONFIRMADA]   DATETIME2(0)   NULL,
        [FECHA_CANCELADA]    DATETIME2(0)   NULL,
        [CANCELADO_POR]      VARCHAR(20)    NULL,
        [PORCENTAJE_REEMB]   INT            NULL,
        CONSTRAINT [PK_RESERVAS] PRIMARY KEY CLUSTERED ([ID_RESERVA]),
        CONSTRAINT [FK_RESERVAS_USUARIOS] FOREIGN KEY ([ID_USER]) REFERENCES [dbo].[USUARIOS]([ID_USER]),
        CONSTRAINT [FK_RESERVAS_SLOTS] FOREIGN KEY ([ID_SLOT]) REFERENCES [dbo].[SLOTS]([ID_SLOT]),
        CONSTRAINT [FK_RESERVAS_HORARIOS] FOREIGN KEY ([ID_HORARIO]) REFERENCES [dbo].[HORARIOS]([ID_HORARIO]),
        CONSTRAINT [FK_RESERVAS_CANCHAS] FOREIGN KEY ([ID_CANCHA]) REFERENCES [dbo].[CANCHAS]([ID_CANCHA]),
        CONSTRAINT [FK_RESERVAS_DUENOS] FOREIGN KEY ([ID_DUENO]) REFERENCES [dbo].[DUENOS]([ID_DUENO]),
        CONSTRAINT [CK_RESERVAS_ESTADO] CHECK ([ESTADO] IN ('PENDIENTE','CONFIRMADA','CANCELADA','EXPIRADA','REEMBOLSADA')),
        CONSTRAINT [CK_RESERVAS_MONTOS] CHECK ([PRECIO_BASE] >= 0 AND [COMISION_QR] >= 0 AND [DESCUENTO] >= 0 AND [MONTO_TOTAL] >= 0),
        CONSTRAINT [CK_RESERVAS_REEMBOLSO] CHECK ([PORCENTAJE_REEMB] IS NULL OR ([PORCENTAJE_REEMB] BETWEEN 0 AND 100)),
        CONSTRAINT [CK_RESERVAS_CANCELADO_POR] CHECK ([CANCELADO_POR] IS NULL OR [CANCELADO_POR] IN ('CLIENTE','DUENO','ADMIN','SISTEMA'))
    );

    CREATE UNIQUE NONCLUSTERED INDEX [UX_RESERVAS_SLOT_ACTIVO]
    ON [dbo].[RESERVAS]([ID_SLOT])
    WHERE [ESTADO] IN ('PENDIENTE','CONFIRMADA');

    CREATE TABLE [dbo].[OFERTAS](
