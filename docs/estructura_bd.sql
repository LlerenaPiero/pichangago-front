        CONSTRAINT [CK_LOCALES_LONGITUD] CHECK ([LONGITUD] IS NULL OR ([LONGITUD] BETWEEN -180 AND 180)),
        CONSTRAINT [CK_LOCALES_UBIGEO] CHECK ([UBIGEO] IS NULL OR (LEN([UBIGEO]) = 6 AND [UBIGEO] NOT LIKE '%[^0-9]%'))
    );

    CREATE TABLE [dbo].[TIPOS_CANCHA](
        [ID_TIPO_CANCHA]       CHAR(10)       NOT NULL,
