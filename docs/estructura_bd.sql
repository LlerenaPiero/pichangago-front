SET QUOTED_IDENTIFIER ON;
SET XACT_ABORT ON;
GO

BEGIN TRY
    BEGIN TRANSACTION;

    /* ============================================================
       1. LIMPIEZA DE TABLAS EXISTENTES
       Se eliminan en orden inverso a las dependencias por FK.
       ============================================================ */

    DROP TABLE IF EXISTS [dbo].[COMPROBANTES];
    DROP TABLE IF EXISTS [dbo].[REEMBOLSOS];
    DROP TABLE IF EXISTS [dbo].[PAGOS];
    DROP TABLE IF EXISTS [dbo].[REVIEWS];
    DROP TABLE IF EXISTS [dbo].[OFERTAS];
    DROP TABLE IF EXISTS [dbo].[RESERVAS];
    DROP TABLE IF EXISTS [dbo].[SLOTS];
    DROP TABLE IF EXISTS [dbo].[HORARIOS];
    DROP TABLE IF EXISTS [dbo].[FOTOS_CANCHA];
    DROP TABLE IF EXISTS [dbo].[CANCHAS];
    DROP TABLE IF EXISTS [dbo].[LOCALES];
    DROP TABLE IF EXISTS [dbo].[TIPOS_CANCHA];
    DROP TABLE IF EXISTS [dbo].[LIQUIDACIONES];
    DROP TABLE IF EXISTS [dbo].[SUSCRIPCIONES];
    DROP TABLE IF EXISTS [dbo].[TOKENS_RECUPERACION];
    DROP TABLE IF EXISTS [dbo].[DUENOS];
    DROP TABLE IF EXISTS [dbo].[USUARIOS];

    /* ============================================================
       2. TABLAS PRINCIPALES
       ============================================================ */

    CREATE TABLE [dbo].[USUARIOS](
