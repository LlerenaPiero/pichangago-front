        CONSTRAINT [CK_USUARIOS_ESTADO] CHECK ([ESTADO] IN ('ACTIVO','INACTIVO','BLOQUEADO'))
    );

    CREATE TABLE [dbo].[DUENOS](
        [ID_DUENO]          CHAR(10)       NOT NULL,
        [ID_USER]           CHAR(10)       NOT NULL,
